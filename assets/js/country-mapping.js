function flagEmojiByTeamName(teamName) {
    teamName = teamName.trim();
    const countryCode = teamNameToTwoLetterCode[teamName];
    const existsNonCountry = nonCountryTeams.includes(teamName)
    if (countryCode) {
        return `<span title="${teamName}" class="fi fi-${countryCode.toLowerCase()}"></span>`;
    }
    else if (existsNonCountry) {
        return `<span title="${teamName}"><strong>[${teamName}]</strong><span>`
    }

    return "";
}

function flagEmojiByThreeLetterCode(threeLetterCode) {
    const alpha2Code = threeLetterCodeToTwoLetterCode[threeLetterCode];
    if (alpha2Code)
        return flagEmojiByTwoLetterCode(alpha2Code);

    return "";
}

function flagEmojiByTwoLetterCode(twoLetterCode) {
    const country = twoLetterCodeToCountryName[twoLetterCode];
    return `<span title="${country}" class="fi fi-${twoLetterCode.toLowerCase()}"></span>`;
}
